<template>
    <div>
        <input type="text" v-model="newTodoItem">
        <span class="addContainer" v-on:click="addTodo">
            <i class="addBtn fas fa-plus" aria-hidden="true"></i>
        </span>
    </div>
</template>

<script>
export default {
    data : function(){
        return{
            newTodoItem : ""
        }

    },
    methods : {
        addTodo: function(){
            //저장하는 로직
            localStorage.setItem(this.newTodoItem,this.newTodoItem);
            this.clearInput();
        },
        clearInput : function(){
            this.newTodoItem = '';
        }
    }
}
</script>

